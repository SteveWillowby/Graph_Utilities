import networkx as nx
import math

def graph_from_srg_string(the_string):
    full_len = len(the_string)
    num_nodes = int(math.sqrt(full_len))
    G = nx.Graph()
    for i in range(0, num_nodes):
        G.add_node(i)
    for i in range(0, num_nodes):
        for j in range(i + 1, num_nodes):
            if the_string[i * num_nodes + j] == '1':
                G.add_edge(i, j)
    return G


